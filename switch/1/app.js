const num = +prompt("Введите Num");

switch (num) { 
    case 1:
        alert("Весна");
        break;
    case 2:
        alert("Лето");
        break;
    case 3:
        alert("Осень");
        break;
    case 4:
        alert("Зима");
        break;
    default:
        alert("Введите число от 1 до 4");   
}